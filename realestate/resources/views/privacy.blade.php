            $(function () {
                $('a[href*="#"]').on("click", function () {
                    if (location.pathname.replace(/^\//, '') == this.pathname.replace(/^\//, '')) {
                        var target = $(this.hash);
                        target = target.length ? target : $('[name=' + this.hash.slice(1) + ']');
                        if (target.length) {
                            $('html,body').animate({
                                scrollTop: target.offset().top
                            }, 1000);
                            return false;
                        }
                    }
                });
            });

